package com.example.fastuae.model;

public class SpecialOffersModel {

    String id;
    String title_name;
    String description;
    String offer_validity;
    String offer_code;
    String image_alt_text;
    String status;
    String image;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle_name() {
        return title_name;
    }

    public void setTitle_name(String title_name) {
        this.title_name = title_name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getOffer_validity() {
        return offer_validity;
    }

    public void setOffer_validity(String offer_validity) {
        this.offer_validity = offer_validity;
    }

    public String getOffer_code() {
        return offer_code;
    }

    public void setOffer_code(String offer_code) {
        this.offer_code = offer_code;
    }

    public String getImage_alt_text() {
        return image_alt_text;
    }

    public void setImage_alt_text(String image_alt_text) {
        this.image_alt_text = image_alt_text;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
