package com.example.fastuae.model;

public class CarModel {

    String image;
    String name;
    String group;
    String model;
    String type;
    String seat;
    String engine;
    String dore;
    String petrol;
    String suitcase;
    String aedNow;
    String aedLater;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getGroup() {
        return group;
    }

    public void setGroup(String group) {
        this.group = group;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSeat() {
        return seat;
    }

    public void setSeat(String seat) {
        this.seat = seat;
    }

    public String getEngine() {
        return engine;
    }

    public void setEngine(String engine) {
        this.engine = engine;
    }

    public String getDore() {
        return dore;
    }

    public void setDore(String dore) {
        this.dore = dore;
    }

    public String getPetrol() {
        return petrol;
    }

    public void setPetrol(String petrol) {
        this.petrol = petrol;
    }

    public String getSuitcase() {
        return suitcase;
    }

    public void setSuitcase(String suitcase) {
        this.suitcase = suitcase;
    }

    public String getAedNow() {
        return aedNow;
    }

    public void setAedNow(String aedNow) {
        this.aedNow = aedNow;
    }

    public String getAedLater() {
        return aedLater;
    }

    public void setAedLater(String aedLater) {
        this.aedLater = aedLater;
    }
}
